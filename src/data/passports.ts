import type { PassportBlock } from '@/types'

// Хелперы для краткой записи блоков паспорта
const h = (text: string): PassportBlock => ({ blockType: 'heading', text })
const rows = (...items: [string, string][]): PassportBlock => ({
  blockType: 'rows',
  items: items.map(([label, value]) => ({ label, value })),
})
const table = (headers: string[], data: string[][]): PassportBlock => ({
  blockType: 'table',
  columns: headers.map((header) => ({ header })),
  data: data.map((r) => ({ cells: r.map((value) => ({ value })) })),
})
const list = (...items: string[]): PassportBlock => ({
  blockType: 'list',
  items: items.map((text) => ({ text })),
})
const note = (text: string): PassportBlock => ({ blockType: 'note', text })
const para = (text: string): PassportBlock => ({ blockType: 'text', text })

